"""
    This file to explore and understand the methods of dictionary in python. 
"""
dict = {
    "ardy": 23,
    "mike": 10,
    "sion": 11,
    "faithlin": 12,
}


print(dict.values())
# [23, 10, 12, 11]
print(dict.keys())
# ['ardy', 'mike', 'faithlin', 'sion']
print(dict["ardy"])
# 23
print(len(dict))
# 4